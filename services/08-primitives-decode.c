/**

DECODE TEST

Decodes the fixed closures that were generated by Elixir GM.

*/

#include "jb_storage.h"
#include "jb_asset.h"
#include "jb_codec.h"

#include <stdio.h>
#include <string.h>
#include <stdlib.h>

typedef struct {
    char* type;
    uint8_t* data;
    uint64_t data_len;
    uint64_t value;
    bool success;
} static_closure_t;

static_closure_t closures[500];

void run_test();

void jb_hook_accumulate(jb_accumulate_arguments_t*) {
    run_test();
}

void jb_hook_refine() {
    run_test();
}

void run_test() {
    for (int i = 0; i < 500; i++) {
        jb_result_t r = JB_ERR_UNREACHABLE;
        uint64_t value;
        static_closure_t closure = closures[i];
        printf("Decoding closure %d: %s\n", i, closure.type);

        if (strcmp(closure.type, "u8") == 0) {
            uint8_t v;
            r = jb_codec_decode_u8(&closure.data, &closure.data_len, &v);
            value = v;
        } else if (strcmp(closure.type, "u16") == 0) {
            uint16_t v;
            r = jb_codec_decode_u16(&closure.data, &closure.data_len, &v);
            value = v;
        } else if (strcmp(closure.type, "u32") == 0) {
            uint32_t v;
            r = jb_codec_decode_u32(&closure.data, &closure.data_len, &v);
            value = v;
        } else if (strcmp(closure.type, "u64") == 0) {
            uint64_t v;
            r = jb_codec_decode_u64(&closure.data, &closure.data_len, &v);
            value = v;
        } else if (strcmp(closure.type, "general_int") == 0) {
            uint64_t v;
            r = jb_codec_decode_general_int(&closure.data, &closure.data_len, &v);
            value = v;
        }

        jb_assert_ok(r, "Failed to decode");
        jb_assert_equal(value, closure.value, "Decoded u8 value mismatch");
        jb_assert_equal(closure.data_len, 0ul, "All data must be consumed");
    }
}

